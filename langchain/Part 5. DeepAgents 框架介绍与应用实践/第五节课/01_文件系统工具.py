# ============================================================
# 3. read_file 分页机制
# ============================================================
print("\n【read_file 分页机制】")
print("-" * 50)

print("""
分页参数：
  - offset：偏移量（行数）
  - limit：每次读取的行数（默认 500）

作用：
  - 强制 Agent 对大文件进行分页读取
  - 避免一次性读爆上下文
  - 适合处理大型日志文件

示例：
  第一次：read_file("/large_log.txt", offset=0, limit=500)
  第二次：read_file("/large_log.txt", offset=500, limit=500)
  ...
""")

# ============================================================
# 4. edit_file 精确替换
# ============================================================
print("\n【edit_file 精确替换】")
print("-" * 50)

print("""
参数：
  - path：文件路径
  - old_string：要被替换的字符串
  - new_string：替换后的字符串
  - replace_all：是否全部替换（默认 False）

防呆设计：
  - 要求先读后改
  - 避免误操作覆盖整个文件

示例：
  edit_file(
      path="/config.py",
      old_string="DEBUG = True",
      new_string="DEBUG = False"
  )
""")

