"""
【案例 1】文件系统工具
=======================

本案例展示 DeepAgents 文件系统中间件的核心工具。

组件：FilesystemMiddleware

核心工具集：
1. ls - 浏览目录结构
2. read_file - 读取文件内容（支持分页）
3. write_file - 创建新文件
4. edit_file - 修改文件（精确替换）
5. glob - 通配符查找文件
6. grep - 正则表达式搜索
7. execute - 执行 Shell 命令

亮点功能：大结果自动转存
- 结果过长时自动拦截
- 写入文件系统（如 /large_tool_results/{id}）
- 只返回摘要和文件路径

要点：
1. 掌握各工具的用途
2. 理解分页读取机制
3. 了解大结果自动转存
"""

