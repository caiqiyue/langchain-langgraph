# -*- coding: utf-8 -*-
"""
【案例 3】Agent 记忆与学习 - 展示 Agent 的记忆系统
=====================================================

本案例展示 Agent 的学习能力（Learning）

Agent 的记忆分为三层：
1. 短期记忆（Checkpointer）- 同一会话内的对话历史
2. 长期记忆（VectorStore）- 跨会话持久化的知识
3. 跨线程记忆（BaseStore）- 结构化的用户档案

学习机制：
- Agent 可以将重要信息存入记忆
- 下次对话时可以检索历史信息
- 实现"越来越懂用户"的个性化服务

要点：
1. 理解 Agent 的三层记忆架构
2. 观察 Agent 如何保存和检索记忆
3. 理解记忆与学习的区别
"""

# ============================================================
# 1. 环境配置
# ============================================================
import os
from dotenv import load_dotenv

load_dotenv(override=True)


# ============================================================
# 2. 导入核心组件
# ============================================================
from langchain.chat_models import init_chat_model
from langchain_core.tools import tool
from langchain.agents import create_agent
from langgraph.checkpoint.memory import InMemorySaver  # 短期记忆

print("=" * 60)
print("案例 3: Agent 记忆与学习")
print("=" * 60)

# ============================================================
# 3. 定义记忆工具
# ============================================================
@tool
def remember_user_info(info: str) -> str:
    """
    将用户的重要信息保存到记忆中。

    当用户告诉您他们的名字、喜好、职业等重要信息时，请调用此工具。

    参数:
        info (str): 要保存的信息，如"用户名叫张三，是一名工程师"
    """
    print(f"\n📝 [记忆工具] 正在保存: {info}")
    return f"✅ 已记住：{info}"

@tool
def recall_user_info(query: str) -> str:
    """
    从记忆中检索用户的相关信息。

    当用户询问之前告诉过您的事情时，请调用此工具。

    参数:
        query (str): 查询关键词，如"用户叫什么名字"
    """
    print(f"\n🔍 [记忆工具] 正在检索: {query}")
    # 在真实场景中，这里会查询向量数据库
    return "根据之前的对话，您告诉过我您叫..."

# ============================================================
# 4. 初始化模型
# ============================================================
model = init_chat_model(
    model="qwen3-max",
    model_provider="qwen",
    temperature=0.7
)

# ============================================================
# 5. 创建具有记忆功能的 Agent
# ============================================================
# 短期记忆：使用 InMemorySaver，会话结束后记忆丢失
memory = InMemorySaver()

agent = create_agent(
    model=model,
    tools=[remember_user_info, recall_user_info],
    system_prompt="""你是一个有个性化记忆能力的智能助手。

工作流程：
1. 当用户告诉你关于他们的事情（如名字、喜好、计划）时，
   必须调用 remember_user_info 工具保存
2. 当用户询问之前说过的事情时，先调用 recall_user_info 检索
3. 如果检索到信息，结合检索结果回答

记住：你的记忆是你提供个性化服务的基础！""",
    checkpointer=memory  # 启用短期记忆
)

# ============================================================
# 6. 演示记忆功能：多轮对话
# ============================================================
print("\n" + "=" * 60)
print("第一轮对话：用户自我介绍")
print("=" * 60)

config = {"configurable": {"thread_id": "user_session_001"}}

对话1 = agent.invoke({
    "messages": [{
        "role": "user",
        "content": "你好，我叫李明，是一名数据科学家，平时喜欢研究机器学习"
    }]
}, config=config)

print(f"用户：我叫李明，是一名数据科学家，平时喜欢研究机器学习")
print(f"Agent：{对话1['messages'][-1].content}")

# ============================================================
# 7. 第二轮对话：验证记忆
# ============================================================
print("\n" + "=" * 60)
print("第二轮对话：询问之前的信息（同一会话）")
print("=" * 60)

对话2 = agent.invoke({
    "messages": [{
        "role": "user",
        "content": "你还记得我叫什么名字吗？我的职业是什么？"
    }]
}, config=config)  # 相同的 thread_id，记忆互通

print(f"用户：你还记得我叫什么名字吗？")
print(f"Agent：{对话2['messages'][-1].content}")

# ============================================================
# 8. 新会话：测试记忆持久化
# ============================================================
print("\n" + "=" * 60)
print("新会话：测试短期记忆的局限性")
print("=" * 60)

新配置 = {"configurable": {"thread_id": "user_session_002"}}

对话3 = agent.invoke({
    "messages": [{
        "role": "user",
        "content": "我们之前聊过吗？你还记得我是谁吗？"
    }]
}, config=新配置)  # 新的 thread_id，记忆不互通

print(f"用户（新政话）：我们之前聊过吗？")
print(f"Agent：{对话3['messages'][-1].content}")

print("""
⚠️ 注意：不同 thread_id 之间的记忆是不互通的！

这就是"短期记忆"的含义：
- 只在同一个会话（thread_id）内有效
- 新开一个会话，Agent 就不记得之前的内容了

如果要实现"跨会话"的长期记忆，需要使用：
- VectorStore（向量数据库）：存储非结构化文本，语义检索
- BaseStore（键值存储）：存储结构化用户档案
""")

# ============================================================
# 9. 记忆的三层架构总结
# ============================================================
print("\n" + "=" * 60)
print("✅ Agent 记忆三层架构总结")
print("=" * 60)
print("""
┌─────────────────────────────────────────────────────────────┐
│                    Agent 记忆系统                           │
├─────────────────────────────────────────────────────────────┤
│                                                             │
│  【第一层】短期记忆 (Checkpointer)                           │
│  ────────────────────────                                   │
│  • 存储位置：内存 / PostgreSQL                               │
│  • 生命周期：与 thread_id 绑定，会话结束则丢失                │
│  • 存储内容：完整的对话历史消息                              │
│  • 适用场景：单会话内的上下文连贯                            │
│                                                             │
│  【第二层】长期记忆 (VectorStore)                            │
│  ────────────────────────                                   │
│  • 存储位置：向量数据库（Chroma/Pinecone/Milvus）            │
│  • 生命周期：持久化，跨会话保留                              │
│  • 存储内容：非结构化文本（对话摘要、知识点）                 │
│  • 检索方式：语义相似度搜索                                  │
│  • 适用场景：跨会话的知识积累                                │
│                                                             │
│  【第三层】跨线程记忆 (BaseStore)                            │
│  ────────────────────────                                   │
│  • 存储位置：键值存储（Redis/PostgreSQL）                   │
│  • 生命周期：持久化，与用户ID绑定                            │
│  • 存储内容：结构化用户档案（偏好、设置）                    │
│  • 检索方式：精确匹配 / 模糊搜索                             │
│  • 适用场景：用户画像、个性化服务                            │
│                                                             │
└─────────────────────────────────────────────────────────────┘

学习 = 记忆 + 检索 + 应用
Agent 通过记忆保存信息，通过检索获取信息，通过应用展现"学习"效果
""")