"""
【案例 1】Agentic RAG 系统概述
================================

Agentic RAG 是将 Agent 引入 RAG 流程的架构，
让 LLM 能够自主规划、迭代优化检索策略。

核心特点：
1. 主动规划：LLM 决定是否检索、如何检索
2. 多轮迭代：根据结果决定是否需要补充检索
3. 工具调用：将检索封装为 Tool 由 LLM 调用

对比传统 RAG：
| 维度 | 传统 RAG | Agentic RAG |
|------|----------|-------------|
| 检索次数 | 单次 | 多轮迭代 |
| LLM 角色 | 被动接收 | 主动决策 |
| 工具调用 | 无 | 有 |
| 复杂问题 | 局限 | 支持 |

要点：
1. 理解 Agentic RAG 的核心思想
2. 掌握 Agentic RAG 的优势
3. 理解应用场景
"""

