print("-" * 50)

print("""
【传统 RAG 流程】
用户问题 → 检索 → 拼接上下文 → LLM 生成回答

局限性：
  1. 一次检索可能不够（复杂问题）
  2. 检索结果可能不相关
  3. 无法判断检索是否足够
  4. 无法多源检索（知识库+网络）

─────────────────────────────────────────────────────────────

【Agentic RAG 流程】
用户问题 → LLM 决策 → 是否检索？ → 工具调用 → 评估结果
                                              ↓
                                         补充检索？ → ...
                                              ↓
                                         生成回答

优势：
  1. LLM 自主决定是否需要检索
  2. 可以多轮检索直到结果足够
  3. 可以评估检索结果的相关性
  4. 可以调用多个工具（知识库+网络）
""")

# ============================================================
# 3. Agentic RAG 核心组件
# ============================================================
print("\n【Agentic RAG 核心组件】")
print("-" * 50)

组件表 = """
| 组件 | 作用 |
|------|------|
| Agent | 核心决策单元，控制工具调用 |
| RAG Tool | 将检索封装为可调用工具 |
| Middleware | 提供上下文压缩、重试、日志等 |
| LLM | 理解问题、评估结果、生成回答 |
"""
