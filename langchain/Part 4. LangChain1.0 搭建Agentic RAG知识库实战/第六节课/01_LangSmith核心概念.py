"""
【案例 1】LangSmith 核心概念
===============================

LangSmith 是 LangChain 官方提供的可观测性平台，
用于追踪、调试和优化 LLM 应用。

核心概念：
1. Project：项目，包含多个 Trace
2. Trace：完整调用链，从输入到输出
3. Run：单个执行节点（LLM调用、工具调用等）
4. Feedback：质量评估（分数/标签）
5. Metadata：元数据（版本、环境等）

层级关系：Project > Trace > Run > Feedback

要点：
1. 理解 LangSmith 的层级结构
2. 掌握 Trace 和 Run 的区别
3. 理解 Feedback 和 Metadata 的作用
"""

