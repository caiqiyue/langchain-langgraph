print("-" * 50)

print("""
Feedback（反馈）：
  - 对 Run 的质量评分
  - 标签（如 "answer_correctness"）
  - 分数（0-1 或分类）
  - 可人工或自动生成

Metadata（元数据）：
  - 附加在 Run 上的键值对
  - {"version": "v1.0", "env": "production"}
  - 用于分类和筛选

用途：
  - Feedback：构建评估数据集，驱动优化
  - Metadata：对比不同版本的性能差异
""")

