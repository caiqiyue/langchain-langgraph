# ============================================================
print("\n【层级结构】")
print("-" * 50)

print("""
LangSmith 层级关系：

Project（项目）
  │
  └── Trace（轨迹）← 一次完整执行
         │
         └── Run（运行）← 单个节点
                │
                └── Feedback（反馈）← 质量评估
                       │
                       └── Metadata（元数据）

解释：
  - Project：你的应用或服务
  - Trace：一次用户请求的完整处理过程
  - Run：Trace 中的单个操作（LLM调用、工具调用等）
  - Feedback：对 Run 的质量评分
  - Metadata：附加信息（版本、用户ID等）
""")

# ============================================================
# 3. Trace 详解
# ============================================================
print("\n【Trace 详解】")
print("-" * 50)

print("""
Trace 包含：
  1. 输入：用户问题
  2. 输出：最终回答
  3. 中间步骤：
     - LLM 决策
     - 工具调用
     - 检索结果
     - 迭代过程
  4. 元数据：
     - 执行时间
     - Token 消耗
     - 模型名称

Trace 用途：
  - 调试：看到每一步的执行
  - 性能分析：识别瓶颈
  - 成本追踪：统计 Token 消耗
""")

# ============================================================
# 4. Run 详解
# ============================================================
print("\n【Run 详解】")
print("-" * 50)

print("""
Run 类型：
  - LLM Run：模型调用
  - Tool Run：工具调用
  - Chain Run：链式调用
  - Agent Run：Agent 执行

Run 信息：
  - 输入/输出
  - 执行时间
  - Token 使用
  - 错误信息

父子关系：
  Root Run
    ├── LLM Run 1
    │    └── Tool Run 1
    │         └── Tool Run 2
    └── LLM Run 2
""")

# ============================================================
# 5. Feedback 和 Metadata
# ============================================================
