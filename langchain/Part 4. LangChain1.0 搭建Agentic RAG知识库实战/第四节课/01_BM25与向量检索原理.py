# ============================================================
print("\n【BM25 原理】")
print("-" * 50)

print("""
BM25（Best Matching 25）算法：

公式：Score(D, Q) = Σ IDF(qi) × (tf(t, D) × (k1 + 1)) / (tf(t, D) + k1 × (1 - b + b × |D|/avgdl))

其中：
  - TF(t, D)：词 t 在文档 D 中的词频
  - IDF(qi)：逆文档频率
  - k1, b：调节参数（通常 k1=1.5, b=0.75）
  - |D|：文档长度
  - avgdl：平均文档长度

核心思想：
  - 词在文档中出现越多，越相关
  - 但词太常见（the, is）不重要，需要 IDF 抑制
  - 长文档会稀释关键词影响，需要长度归一化
""")

