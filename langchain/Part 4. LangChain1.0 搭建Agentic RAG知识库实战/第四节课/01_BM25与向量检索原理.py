# 3. 向量检索原理
# ============================================================
print("\n【向量检索原理】")
print("-" * 50)

print("""
向量检索流程：

1. Embedding：将文本转换为向量
   "LangChain 是什么" → [0.12, -0.34, 0.56, ..., 0.78]

2. 索引：将文档向量存入向量数据库
   doc1_vector, doc2_vector, doc3_vector, ...

3. 查询：
   query → embed_query → query_vector
   query_vector 与所有 doc_vector 计算相似度
   返回最相似的 top-k

常用相似度度量：
  - 余弦相似度：cos(θ) = A·B / (|A| × |B|)
  - 点积：直接计算向量内积
  - 欧氏距离：||A - B||

FAISS 加速技术：
  - IVF（倒排索引）：先聚类再搜索
  - HNSW（层次导航）：图索引快速导航
""")

