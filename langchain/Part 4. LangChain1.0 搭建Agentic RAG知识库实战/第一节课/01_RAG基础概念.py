"""
【案例 1】RAG 基础概念 - 理解 Retrieval Augmented Generation
===============================================================

RAG = Retrieval（检索）+ Augmented（增强）+ Generation（生成）

核心思想：
让 LLM 在回答问题前，先从知识库中检索相关信息，
将检索结果作为上下文加入提示词，从而生成更准确的答案。

要点：
1. 理解 RAG 的三个核心组件
2. 掌握 RAG 的完整工作流程
3. 理解为什么需要 RAG
"""

