# ============================================================
print("""
┌─────────────────────────────────────────────────────────────┐
│                    RAG 核心组件                               │
├─────────────────────────────────────────────────────────────┤
│                                                             │
│  1. Retriever（检索器）                                     │
│     - 从向量数据库中检索相关文档                             │
│     - 使用语义相似度匹配                                     │
│     - 返回 Top-K 个最相关的文档                             │
│                                                             │
│  2. Retriever + LLM（增强）                                │
│     - 将检索结果格式化为上下文                               │
│     - 构建增强后的提示词                                     │
│     - 包含原始问题和检索文档                                 │
│                                                             │
│  3. LLM（生成器）                                          │
│     - 基于增强后的提示词生成答案                             │
│     - 参考检索到的知识，而非仅依赖训练数据                   │
│                                                             │
└─────────────────────────────────────────────────────────────┘
""")

