print("-" * 50)

print("""
问题1：谁来决定"做不做"？

❓ 需要 LLM 根据上下文判断是否执行
   → 应该封装为 Tool
   示例：搜索互联网、查询天气
   原因：用户可能只是打招呼，不需要搜索

✅ 业务流程规定必须执行
   → 应该封装为 Middleware / Node
   示例：权限校验、敏感词过滤、日志记录
   原因：每个请求都要执行

─────────────────────────────────────────────────────────────

问题2：参数从哪里来？

❓ 参数需要从用户的自然语言中提取
   → 应该封装为 Tool
   示例：用户说"帮我查下特斯拉的股价"
   原因：Tool 需要提取"特斯拉"作为参数

✅ 参数是系统上下文或环境变量
   → 应该封装为 Middleware / Node
   示例：UserID、SessionID、数据库配置
   原因：不需要 LLM 去"猜"

─────────────────────────────────────────────────────────────

问题3：失败了怎么办？

❓ 失败了需要 LLM 换个方式重试
   → 应该封装为 Tool
   示例：搜索不到结果，LLM 尝试换个关键词
   原因：LLM 可以自主调整策略

✅ 失败了直接抛异常或走系统降级
   → 应该封装为 Middleware / Node
   示例：数据库连接断开、API 鉴权失败
   原因：不需要 LLM 介入
""")

