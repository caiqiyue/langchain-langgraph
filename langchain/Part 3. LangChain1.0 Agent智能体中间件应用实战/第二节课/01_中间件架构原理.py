print("=" * 60)

hook_详解 = """
┌──────────────────┬────────────────────────────────────────┐
│ Hook 点          │ 作用                                   │
├──────────────────┼────────────────────────────────────────┤
│ before_agent     │ Agent 执行前最后机会修改输入             │
│ before_model     │ 模型调用前修改 prompt / 选择模型       │
│ wrap_model_call │ 包裹模型调用（异常处理、重试逻辑）       │
│ wrap_tool_call  │ 包裹工具调用（异常处理、重试逻辑）       │
│ after_model     │ 模型调用后修改输出                     │
│ after_agent     │ Agent 执行完成后的最终处理              │
└──────────────────┴────────────────────────────────────────┘
"""
print(hook_详解)

